import { AttachmentBuilder, PermissionFlagsBits, SlashCommandBuilder } from "discord.js";
import { getSupportTicket, isSupportTicket } from "../utils/support-tickets.js";

export const data = new SlashCommandBuilder()
  .setName("ticket-transcript")
  .setDescription("Envoie en MP le transcript texte du ticket courant.")
  .setDefaultMemberPermissions(PermissionFlagsBits.ManageChannels);

export async function execute(interaction) {
  const channel = interaction.channel;
  const guild = interaction.guild;

  if (!guild || !channel || !channel.isTextBased()) {
    await interaction.reply({
      content: "Cette commande doit etre utilisee dans un ticket textuel de serveur.",
      ephemeral: true,
    });
    return;
  }

  if (!isSupportTicket(channel.id)) {
    await interaction.reply({
      content: "Ce salon n'est pas un ticket gere par le bot.",
      ephemeral: true,
    });
    return;
  }

  const ticket = getSupportTicket(channel.id);
  const messages = await channel.messages.fetch({ limit: 100 });
  const transcript = messages
    .sort((left, right) => left.createdTimestamp - right.createdTimestamp)
    .map((message) => {
      const createdAt = new Date(message.createdTimestamp).toISOString();
      const content = message.content?.trim() || "[sans contenu texte]";
      return `[${createdAt}] ${message.author.tag}: ${content}`;
    })
    .join("\n");

  const header = [
    `Guild: ${guild.name} (${guild.id})`,
    `Channel: #${"name" in channel ? channel.name : channel.id} (${channel.id})`,
    `Owner: ${ticket?.owner ?? "unknown"}`,
    `Language: ${ticket?.language ?? "n/a"}`,
    `Topic: ${ticket?.topic ?? "n/a"}`,
    `Generated by: ${interaction.user.tag}`,
    "",
  ].join("\n");

  const attachment = new AttachmentBuilder(
    Buffer.from(`${header}${transcript || "Aucun message disponible."}`, "utf8"),
    { name: `ticket-${channel.id}-transcript.txt` },
  );

  try {
    await interaction.user.send({
      content: `Transcript du ticket ${channel}.`,
      files: [attachment],
    });

    await interaction.reply({
      content: "Le transcript du ticket a ete envoye en MP.",
      ephemeral: true,
    });
  } catch {
    await interaction.reply({
      content: "Impossible d'envoyer le transcript en MP. Verifiez que vos messages prives sont ouverts.",
      ephemeral: true,
    });
  }
}
